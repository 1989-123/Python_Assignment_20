"""
Write a python program to create a function to 
check whether a string is a pangram or not.
"""

def f1(str):
    if 'a' 'b' 'c' 'd' 'e' 'f' 'g' 'h' 'i' 'j' 'k' 'l' 'm' 'n' 'o' 'p' 'q' 'r' 's' 't' 'u' 'v' 'w' 'x' 'y' 'z' in str:
        print("String is pangram")
